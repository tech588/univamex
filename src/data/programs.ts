export function getProgramBySlug(slug: string) {
  return programs.find((program) => program.slug === slug);
}

export function getProgramsByLevel(level: Program["level"]) {
  return programs.filter((program) => program.level === level);
}

export function getRelatedPrograms(program: Program) {
  return programs
    .filter(
      (candidate) =>
        candidate.slug !== program.slug &&
        (candidate.area === program.area || candidate.level === program.level),
    )
    .slice(0, 3);
}




